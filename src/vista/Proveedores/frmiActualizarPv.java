    public void enviarRegistro() {
        tableProv.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                if (tableProv.getSelectedRow() != -1) {
                    cargarDatos();
                }
            }
        });

    }

    void cargarDatos() {
        int fila = tableProv.getSelectedRow();
        txt_ProveedorCod.setText(tableProv.getValueAt(fila, 0).toString());
        txt_Cedula.setText(tableProv.getValueAt(fila, 1).toString());
        txt_Nombre.setText(tableProv.getValueAt(fila, 2).toString());
        txt_Direccion.setText(tableProv.getValueAt(fila, 3).toString());
        txt_Telefono.setText(tableProv.getValueAt(fila, 4).toString());

    }

    void marcarFila(String id) {

        for (int i = 0; i < tableProv.getRowCount(); i++) {
            if (id.equals(tableProv.getValueAt(i, 0))) {
                tableProv.setRowSelectionInterval(i, i);
                // break;
            }
        }
    }

