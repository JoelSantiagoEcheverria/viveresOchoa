    private void btn_ActualzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualzarActionPerformed
        if (txt_Cedula.getText().equals("") || txt_Direccion.getText().equals("") || txt_Nombre.getText().equals("")
                || txt_Telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Es obligatorio llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            Toolkit.getDefaultToolkit().beep();
        } else {
            int op = JOptionPane.showConfirmDialog(null, "Está seguro que desea Actualizar",
                    "Opción Actualizar", JOptionPane.OK_OPTION);
            if (op == JOptionPane.OK_OPTION) {
                proveedor = new proveedoresCont(txt_ProveedorCod.getText(), txt_Cedula.getText(), txt_Nombre.getText(),
                         txt_Direccion.getText(), txt_Telefono.getText());
                proveedor.actualizar();
                String fila = txt_ProveedorCod.getText();
                proveedor.buscar("");
                marcarFila(fila);
                this.doDefaultCloseAction();
            }
        }
    }//GEN-LAST:event_btn_ActualzarActionPerformed

