    public void centrar(JInternalFrame ventana) {
        int x = (frmMenu.escritorio.getWidth() / 2) - (ventana.getWidth() / 2);
        int y = (frmMenu.escritorio.getHeight() / 2) - (ventana.getHeight() / 2);

        if (ventana.isVisible()) {
            ventana.setLocation(x, y);
            ventana.toFront();
        } else {
            frmMenu.escritorio.add(ventana);
            ventana.setVisible(true);
            ventana.setLocation(x, y);

        }
    }

    public static void seleccionarFila(String idPv) {

        for (int i = 0; i < tableProv.getRowCount(); i++) {
            if (idPv.equals(tableProv.getValueAt(i, 0))) {
                tableProv.setRowSelectionInterval(i, i);
            }
        }
    }

    public void borrarSeleccion() {
        int fila = frmiCatalogoPv.tableProv.getSelectedRow();
        if (frmiCatalogoPv.tableProv.getSelectedRow() > -1) {
            frmiCatalogoPv.tableProv.removeRowSelectionInterval(fila, fila);
        }
    }

