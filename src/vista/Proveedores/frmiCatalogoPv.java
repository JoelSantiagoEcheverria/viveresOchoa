    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed

        if (tableProv.getSelectedRow() > -1) {
            int fila = tableProv.getSelectedRow();
            codigo = tableProv.getValueAt(fila, 0).toString();
            nombre = tableProv.getValueAt(fila, 2).toString();
            int op = JOptionPane.showConfirmDialog(null, "Está seguro que quiere eliminar a: " + nombre, "Eliminar", JOptionPane.OK_OPTION);
            if (op == JOptionPane.OK_OPTION) {

                proveedores.eliminar();
                proveedores.buscar("");
                proveedoresModel.generarCodigo();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

