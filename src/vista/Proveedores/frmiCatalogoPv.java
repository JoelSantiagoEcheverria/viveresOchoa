    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        if (tableProv.getSelectedRow() > -1) {
            if (!(actualizar instanceof frmiActualizarPv)) {
                actualizar = new frmiActualizarPv();
                actualizar.cargarDatos();

            }
            actualizar.enviarRegistro();
            centrar(actualizar);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_ActualizarActionPerformed

