    public static boolean onlyLetters(String letter) {
        return letter.matches("[0-9]*");
    }
    
    public void seleccionarNuevo(){
        int filas=vista.Usuario.frmiCatalogo.table.getRowCount();
        for (int i = 0; i < filas; i++) {
            if (txt_UsuarioCod.getText().equals(vista.Usuario.frmiCatalogo.table.getValueAt(i, 0))  ) {
                vista.Usuario.frmiCatalogo.table.setRowSelectionInterval(i, i);
                 break;
            }
           
        }
        
    }

