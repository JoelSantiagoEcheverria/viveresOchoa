    private void txt_NombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_NombreKeyReleased

        if (!(onlyLetters(txt_Nombre.getText().trim()))) {
            txt_Nombre.setText(txt_Nombre.getText().toUpperCase());
            lbl_ComprobarNom.setVisible(false);

        } else {
            lbl_ComprobarNom.setText("Escriba el nombre sin números");
            lbl_ComprobarNom.setVisible(true);
            lbl_ComprobarNom.setForeground(Color.RED);
        }


    }//GEN-LAST:event_txt_NombreKeyReleased

