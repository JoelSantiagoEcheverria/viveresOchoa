    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        if (table.getSelectedRow() != -1) {
            if (recuperaCont()) {
                int fila = table.getSelectedRow();
                nombre = table.getValueAt(fila, 1).toString();
                int opcion = JOptionPane.showConfirmDialog(null, "Está seguro que quiere eliminar a: " + nombre, "Eliminar", JOptionPane.OK_OPTION);
                if (opcion == JOptionPane.OK_OPTION) {

                    codigo = table.getValueAt(fila, 0).toString();
                    usu.eliminar();
                    limpiar();
                    usuarioModel.codUsu();
                }
            } else {
                JOptionPane.showMessageDialog(this, "INGRESE LA CONTRASEÑA CORRECTA!!!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

