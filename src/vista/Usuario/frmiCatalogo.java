    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        if (table.getSelectedRow() > -1) {
            if (recuperaCont()) {
                if (!(actualizar instanceof vista.Usuario.frmiActualizar)) {
                    actualizar = new vista.Usuario.frmiActualizar();
                }
                centrar(actualizar);
            } else {
                JOptionPane.showMessageDialog(this, "INGRESE LA CONTRASEÑA CORRECTA!!!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_ActualizarActionPerformed

