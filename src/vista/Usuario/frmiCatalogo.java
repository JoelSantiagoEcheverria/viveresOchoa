    public void centrar(JInternalFrame ventana) {
        int x = (escritorio.getWidth() / 2) - (ventana.getWidth() / 2);
        int y = (escritorio.getHeight() / 2) - (ventana.getHeight() / 2);

        if (ventana.isVisible()) {
            ventana.setLocation(x, y);
            ventana.toFront();
        } else {
            escritorio.add(ventana);
            ventana.setVisible(true);
            ventana.setLocation(x, y);

        }
    }

    public boolean recuperaCont() {
        JPasswordField contrasenia = new JPasswordField();
        JOptionPane.showConfirmDialog(null, contrasenia, "Contraseña de verificación", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        String unirCadenaPass = new String(contrasenia.getPassword());
        int fila = table.getSelectedRow();

        String codUsu = table.getValueAt(fila, 0).toString();
        String password = "";

        Statement st;
        ResultSet rs;

        try {
            st = BD.conexion().createStatement();
            rs = st.executeQuery("SELECT CONTRASENA FROM USUARIOS WHERE CODUSU LIKE'" + codUsu + "'");

            while (rs.next()) {
                password = rs.getString(1);

            }
            return password.equals(unirCadenaPass);

        } catch (SQLException ex) {
            Logger.getLogger(frmiCatalogo.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }

    }

    public void limpiar() {
        int i = table.getSelectedRow();
        txt_Buscar.setText("");

        if (table.getSelectedRow() > -1) {
            table.removeRowSelectionInterval(i, i);
        }
        usu.buscar("");
    }

