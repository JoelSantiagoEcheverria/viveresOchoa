    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        if (txt_Marca.getText().equals("") || txt_NombrePro.getText().equals("") || txt_PrecioCompra.getText().equals("")
                || txt_PrecioVenta.getText().equals("") || txt_Stock.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Es obligatorio llenar todos los campos", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else if (cbo_TipoCategoria.getSelectedIndex() == 0 || cbo_TipoProveedor.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Es obligatorio llenar todos los campos", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            int op = JOptionPane.showConfirmDialog(null, "Está seguro que desea Actualizar", "Opción Actualizar", JOptionPane.OK_OPTION);
            if (op == JOptionPane.OK_OPTION) {

                String categoria = cbo_TipoCategoria.getItemAt(cbo_TipoCategoria.getSelectedIndex()).getIdCategoria();

                String prove = cbo_TipoProveedor.getItemAt(cbo_TipoProveedor.getSelectedIndex()).getCodigo();

                Float pCompra = Float.parseFloat((txt_PrecioCompra.getText()).replaceAll("[^0-9]", "."));
                Float pVenta = Float.parseFloat(txt_PrecioVenta.getText().replaceAll("[^0-9]", "."));
                int stock = Integer.parseInt(txt_Stock.getText());

                p = new productosCont(txt_CodigoPro.getText(), categoria, prove, txt_NombrePro.getText(),
                        txt_Marca.getText(), pCompra, pVenta, stock);
                p.actualizar();
                txt_StockIngreso.setText("");
                String idprod = txt_CodigoPro.getText();
                p.buscar("");
                seleccionar(idprod);
                cajaCont producto = new cajaCont();
                try {
                    producto.buscar("");
                    frmiElegirProd.giveFormat();
                } catch (Exception e) {
                }

            }
            frmiCatalogoProducto.giveFormat();
            int fila = frmiCatalogoProducto.table_Producto.getSelectedRow();
            txt_PrecioCompra.setText(frmiCatalogoProducto.table_Producto.getValueAt(fila, 3).toString());
            txt_PrecioVenta.setText(frmiCatalogoProducto.table_Producto.getValueAt(fila, 4).toString());

            this.doDefaultCloseAction();
        }
    }//GEN-LAST:event_btn_ActualizarActionPerformed

