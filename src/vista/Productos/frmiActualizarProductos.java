    private void txt_StockIngresoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_StockIngresoKeyReleased
        int actual, suma, ingreso, resta;

        seleccionar(txt_CodigoPro.getText());
        String registro = txt_StockIngreso.getText().replaceAll("[^0-9]", "");//admite caracters de 0 al 9 y lo demas remplaz por null
        if (!(registro.equals(""))) {

            switch (txt_StockIngreso.getText().charAt(0)) {
                case '-':
                    ingreso = Integer.parseInt(txt_StockIngreso.getText().replaceAll("[^0-9]", ""));
                    actual = Integer.parseInt(stock());
                    resta = actual - ingreso;
                    txt_Stock.setText(String.valueOf(resta));
                    break;
                case '+':
                    ingreso = Integer.parseInt(txt_StockIngreso.getText());
                    actual = Integer.parseInt(stock());
                    suma = actual + ingreso;
                    txt_Stock.setText(String.valueOf(suma));
                    break;
                default:
                    ingreso = Integer.parseInt(txt_StockIngreso.getText());
                    actual = Integer.parseInt(stock());
                    suma = actual + ingreso;
                    txt_Stock.setText(String.valueOf(suma));
                    break;
            }

        } else {
            txt_Stock.setText(stock());
        }
        /*NOTA -- para remplazar caracteres [^\\d.-] */

    }//GEN-LAST:event_txt_StockIngresoKeyReleased

