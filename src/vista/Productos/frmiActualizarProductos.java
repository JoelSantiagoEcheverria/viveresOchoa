    public void enviarDatos() {
        table_Producto.getSelectionModel().addListSelectionListener((ListSelectionEvent lse) -> {
            if (table_Producto.getSelectedRow() > -1) {

                cargar();
            }
        });
    }

    public void cargar() {
        int fila = table_Producto.getSelectedRow();
        cod = table_Producto.getValueAt(fila, 0).toString();

        for (int i = 1; i < cbo_TipoCategoria.getItemCount(); i++) {

            if (p.verid().equals(cbo_TipoCategoria.getItemAt(i).getIdCategoria())) {
                cbo_TipoCategoria.setSelectedIndex(i);

                break;

            }

        }

        for (int i = 1; i < cbo_TipoProveedor.getItemCount(); i++) {
            if (p.idproveedor().equals(cbo_TipoProveedor.getItemAt(i).getCodigo())) {
                cbo_TipoProveedor.setSelectedIndex(i);

                break;
            }

        }

        txt_CodigoPro.setText(table_Producto.getValueAt(fila, 0).toString());
        txt_NombrePro.setText(table_Producto.getValueAt(fila, 1).toString());
        txt_Marca.setText(table_Producto.getValueAt(fila, 2).toString());
        txt_PrecioCompra.setText(table_Producto.getValueAt(fila, 3).toString());
        txt_PrecioVenta.setText(table_Producto.getValueAt(fila, 4).toString());
        txt_Stock.setText(table_Producto.getValueAt(fila, 5).toString());

    }

    public void seleccionar(String id) {
        for (int i = 0; i < table_Producto.getRowCount(); i++) {
            if (id.equals(table_Producto.getValueAt(i, 0))) {
                table_Producto.setRowSelectionInterval(i, i);
            }
        }
    }

    public String stock() {
        int fila = table_Producto.getSelectedRow();
        String regresar = table_Producto.getValueAt(fila, 5).toString();
        return regresar;
    }

