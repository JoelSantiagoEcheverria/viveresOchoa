    private void txt_DescripcionCatKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_DescripcionCatKeyReleased
        txt_DescripcionCat.setText(txt_DescripcionCat.getText().toUpperCase());
        if (!(letras(txt_DescripcionCat.getText()))) {
            lbl_Letras.setText("Escriba solo letras");
            lbl_Letras.setForeground(Color.RED);
            lbl_Letras.setVisible(true);
        } else {
            lbl_Letras.setVisible(false);
        }
    }//GEN-LAST:event_txt_DescripcionCatKeyReleased

