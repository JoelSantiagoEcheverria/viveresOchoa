    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txt_DescripcionCat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo vacio", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            cat = new categoriaCont(txt_codigoCat.getText(), txt_DescripcionCat.getText());
            cat.guardar();
            txt_DescripcionCat.setText("");
            txt_codigoCat.setText("");
            idCategoria();
            frmiNuevoProductos.cbo_TipoCategoria.removeAllItems();
            categoriaCont.laCategoria(frmiNuevoProductos.cbo_TipoCategoria);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

