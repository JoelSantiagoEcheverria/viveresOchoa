    private void btn_ActualzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualzarActionPerformed
        String cedula, nombre, direccion, telefono;
        cedula = txt_Cedula.getText();
        nombre = txt_Nombre.getText();
        direccion = txt_Direccion.getText();
        telefono = txt_Telefono.getText();
        if (cedula.isEmpty() || nombre.isEmpty() || direccion.isEmpty() || telefono.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Es obligatorio llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            int op = JOptionPane.showConfirmDialog(null, "Está seguro que desea Actualizar", "Opción Actualizar", JOptionPane.OK_OPTION);
            if (op == JOptionPane.OK_OPTION) {
                cliente = new clientesCont(txt_ClienteCod.getText(), txt_Cedula.getText(),
                        txt_Nombre.getText(), txt_Direccion.getText(), txt_Telefono.getText());
                cliente.actualizar();
                String id = txt_ClienteCod.getText();
                cliente.buscar("");
                frmiCatalogo.marcarFila(id);

            }
        }


    }//GEN-LAST:event_btn_ActualzarActionPerformed

