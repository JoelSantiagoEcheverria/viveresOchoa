    public void enviarDatos() {
        frmiCatalogo.table_clientes.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                if (frmiCatalogo.table_clientes.getSelectedRow() != -1) {

                    cargarDatos();

                }
            }
        });

    }

    public void cargarDatos() {

        JTable tabla = frmiCatalogo.table_clientes;
        int fila = tabla.getSelectedRow();

        txt_ClienteCod.setText(tabla.getValueAt(fila, 0).toString());
        txt_Cedula.setText(tabla.getValueAt(fila, 1).toString());
        txt_Nombre.setText(tabla.getValueAt(fila, 2).toString());
        txt_Direccion.setText(tabla.getValueAt(fila, 3).toString());
        txt_Telefono.setText(tabla.getValueAt(fila, 4).toString());
    }

